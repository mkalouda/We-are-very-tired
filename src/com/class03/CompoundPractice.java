package com.class03;

public class CompoundPractice {
	
	public static void main (String[] args){
		
		int b=200;
		
		b+=500;
		System.out.println(b);
		
		b-=67;
		System.out.println(b);
		
		System.out.println("-----------------------");
		
		int cakePiece=11;
		
		cakePiece/=4;
		System.out.println(cakePiece);
		
		System.out.println("------------------------");
		
		int cake=25;
		
		cake%=7;
		System.out.println(cake);
		
	}

}
