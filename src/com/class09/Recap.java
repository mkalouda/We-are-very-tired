package com.class09;

public class Recap {

	public static void main(String[] args) {
		
		for (int i=5; i<7; i++) {
			System.out.println(i);
			for (int j=10; j<=12; j++) {
				System.out.println(j);
			}
		}
		
		for (int i=5; i<7; i++) {
		
			for (int j=10; j<=12; j++) {
				System.out.println(i+" "+j);
			}
		}

	}

}
